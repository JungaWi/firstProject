package control;

public class WhileExe {
	public static void main(String[] args) {
		
		// while 1~10까지 출력
		
		int i = 1;
		while (i<=10); {
			System.out.println(i);
			i++;			
		}		
		System.out.println("끝.");
		
		// 짝수 홀수 출력
		int i2 = 0;
		while (i2<=10) {
			i2++;
			if (i2%2==0) {
				System.out.println(i2);
	
				
			}
		}
		
	}

}
